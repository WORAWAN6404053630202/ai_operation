class AcademicPersonaService:
    """
    Academic Persona (REQUIRED FLOW)
    2.1 Retrieve -> ask REQUIRED SLOTS (dynamic, generated from question + docs)
    2.2 After slots -> ask which SECTIONS (dynamic; only what exists for this case)
    3) Final answer (evidence-only; best-effort)
    4) Mark done for supervisor auto-return

    Production fixes (P0/P1):
    - ✅ Do NOT create academic_flow.stage="idle" automatically
    - ✅ Retrieve ONLY once when starting intake (store academic_question); never re-retrieve on slot replies
    - ✅ Use academic_question for final prompt (never fallback to "1,2" etc.)
    - ✅ Make force_intake meaningful: keep intake stage, re-ask slots on greeting/noise, never reset stage
    - ✅ Stronger greeting/noise detection inside intake (backup safety)
    - ✅ Fix Thai-text false-noise bug (P0): Thai-only responses must NOT be treated as noise
    - ✅ Slots are NOT a numbered menu (P0): do not bind numbers for slot stage
    - ✅ If no sections exist -> answer immediately (P0): do not stall awaiting_sections
    - ✅ NEW: Dynamic slots generated by LLM from user question + docs (not hardcoded template)
    - ✅ NEW: Cross-persona slot memory — skip already-known slots from Practical mode
    - ✅ NEW: Auto-return stores topic context for supervisor to build related topics menu
    - ✅ NEW: LLM call timeout via conf.LLM_REQUEST_TIMEOUT
    """

    persona_id = "academic"

    # For parsing numbered options (used ONLY for section menu, not slots)
    _OPTION_LINE_RE = re.compile(r"(?m)^\s*(\d{1,2})\s*[\)\.\:]\s*(.+?)\s*$")

    # Detect "ทั้งหมด"
    _SELECT_ALL_RE = re.compile(r"(ทั้งหมด|ทุกข้อ|ทุกหัวข้อ|เอาทั้งหมด|all|everything)", re.IGNORECASE)

    # Backup greeting/noise detection (supervisor should already do, but keep safe)
    _TH_LAUGH_RE = re.compile(r"^\s*5{3,}\s*$")
    _EN_GREETING_RE = re.compile(r"^\s*(hi+|hello+|hey+|yo+)\b", re.IGNORECASE)
    _EN_GOOD_TIME_RE = re.compile(r"^\s*good\s+(morning|afternoon|evening|night)\b", re.IGNORECASE)
    _TH_SAWASDEE_FUZZY_RE = re.compile(r"^\s*สว[^\s]{0,6}ดี", re.IGNORECASE)
    _TH_WATDEE_RE = re.compile(r"^\s*หวัดดี", re.IGNORECASE)
    _FILLER_ONLY_RE = re.compile(r"^\s*(ครับ|คับ|ค่ะ|คะ|จ้า|จ้ะ|ค่า|งับ)\s*$", re.IGNORECASE)

    # IMPORTANT P0:
    # Do NOT treat Thai-only text as noise.
    # Only treat as noise when:
    # - very short AND only punctuation/symbols, OR
    # - single repeated character patterns, OR
    # - meaningless latin-only fragments (very short)
    _PUNCT_ONLY_RE = re.compile(r"^\s*[!?.…。，、\-_=+*/\\|@#$%^&*(){}\[\]<>\"'`~:;]+?\s*$")
    _REPEATED_CHAR_RE = re.compile(r"^\s*(.)\1{6,}\s*$")  # e.g., "aaaaaaa", "!!!!!!", "รรรรรรร"
    _LATIN_GIBBERISH_RE = re.compile(r"^\s*[a-z]{1,8}\s*$", re.IGNORECASE)  # tiny latin-only token

    def __init__(self, retriever):
        self.retriever = retriever
        self._init_llm()

    def _init_llm(self):
        model_name = getattr(conf, "OPENROUTER_MODEL_ACADEMIC", conf.OPENROUTER_MODEL)
        timeout = int(getattr(conf, "LLM_REQUEST_TIMEOUT", 30))
        self.llm = ChatOpenAI(
            model=model_name,
            openai_api_key=conf.OPENROUTER_API_KEY,
            openai_api_base=conf.OPENROUTER_BASE_URL,
            temperature=getattr(conf, "TEMPERATURE_ACADEMIC", 0.3),
            max_tokens=getattr(conf, "MAX_TOKENS_ACADEMIC", 10000),
            request_timeout=timeout,
            model_kwargs={"response_format": {"type": "json_object"}},
        )
        # Separate LLM for slot generation.
        # GPT-5.1 is a thinking model — reasoning tokens eat into the budget first.
        # MAX_TOKENS_ACADEMIC_SLOTS (default 3000) gives room for reasoning + actual JSON output.
        slots_max_tokens = getattr(conf, "MAX_TOKENS_ACADEMIC_SLOTS", 3000)
        self.llm_slots = ChatOpenAI(
            model=model_name,
            openai_api_key=conf.OPENROUTER_API_KEY,
            openai_api_base=conf.OPENROUTER_BASE_URL,
            temperature=0.1,
            max_tokens=slots_max_tokens,
            request_timeout=timeout,
            model_kwargs={"response_format": {"type": "json_object"}},
        )

    # ----------------------------
    # Safe append (dedupe) - prefer state helpers when available
    # ----------------------------
    def _append_user_once(self, state: ConversationState, content: str) -> None:
        if hasattr(state, "add_user_message_once"):
            state.add_user_message_once(content)
            return
        if content is None:
            return
        c = str(content)
        if not c.strip():
            return
        if state.messages and state.messages[-1].get("role") == "user" and (state.messages[-1].get("content") or "") == c:
            return
        state.messages.append({"role": "user", "content": c})

    def _append_assistant(self, state: ConversationState, content: str) -> None:
        if hasattr(state, "add_assistant_message_once"):
            state.add_assistant_message_once(content)
            return
        if content is None:
            return
        c = str(content).strip()
        if not c:
            return
        state.messages = state.messages or []
        if state.messages:
            last = state.messages[-1]
            if last.get("role") == "assistant" and (last.get("content") or "").strip() == c:
                return
        state.messages.append({"role": "assistant", "content": c})

    # ----------------------------
    # Context helpers (flow)
    # ----------------------------
    def _get_flow(self, state: ConversationState) -> Optional[Dict[str, Any]]:
        """
        IMPORTANT (P0):
        - Do NOT create flow on read.
        - Return None if flow not started yet.
        """
        state.context = state.context or {}
        flow = state.context.get("academic_flow")
        if isinstance(flow, dict) and str(flow.get("stage") or "").strip():
            return flow
        return None

    def _ensure_flow(self, state: ConversationState, stage: str, **kwargs) -> Dict[str, Any]:
        """
        Create/overwrite flow only when we truly start intake or update a known stage.
        """
        state.context = state.context or {}
        flow = state.context.get("academic_flow")
        if not isinstance(flow, dict):
            flow = {}
        flow["stage"] = stage
        flow.update(kwargs)
        state.context["academic_flow"] = flow
        return flow

    def _set_flow(self, state: ConversationState, **kwargs) -> None:
        """
        Update flow if it exists; create only if 'stage' provided.
        """
        state.context = state.context or {}
        flow = state.context.get("academic_flow")
        if not isinstance(flow, dict):
            flow = {}
        if "stage" in kwargs and kwargs.get("stage"):
            flow["stage"] = kwargs.get("stage")
        for k, v in kwargs.items():
            if k == "stage":
                continue
            flow[k] = v
        if str(flow.get("stage") or "").strip():
            state.context["academic_flow"] = flow

    def _mark_done(self, state: ConversationState) -> None:
        self._set_flow(state, stage="done")
        state.context["auto_return_to_practical"] = True
        # Store topic context so supervisor can build a related-topics menu on auto-return
        topic_ctx = (state.context or {}).get("academic_question", "")
        if topic_ctx:
            state.context["auto_return_topic_context"] = topic_ctx

    # ----------------------------
    # Greeting/noise (backup safety)
    # ----------------------------
    def _looks_like_greeting_or_noise(self, user_text: str) -> bool:
        raw = (user_text or "").strip()
        if not raw:
            return True
        if self._TH_LAUGH_RE.match(raw):
            return True
        if self._FILLER_ONLY_RE.match(raw):
            return True

        low = raw.lower().strip()
        if self._EN_GREETING_RE.match(low) or self._EN_GOOD_TIME_RE.match(low):
            return True
        if self._TH_WATDEE_RE.match(raw) or self._TH_SAWASDEE_FUZZY_RE.match(raw):
            return True

        # P0: do NOT treat Thai-only as noise. Only detect truly noisy patterns.
        if len(raw) <= 16:
            if self._PUNCT_ONLY_RE.match(raw):
                return True
            if self._REPEATED_CHAR_RE.match(raw):
                return True
            if self._LATIN_GIBBERISH_RE.match(raw):
                return True

        return False

    # ----------------------------
    # Retrieval (only once per intake)
    # ----------------------------
    def _retrieve_docs(self, query: str, metadata_filter: Optional[Dict[str, Any]] = None) -> List[Dict[str, Any]]:
        max_docs = int(getattr(conf, "LLM_DOCS_MAX_ACADEMIC", 12) or 12)
        max_chars = int(getattr(conf, "LLM_DOC_CHARS_ACADEMIC", 700) or 700)

        if metadata_filter:
            vectorstore = getattr(self.retriever, "vectorstore", None)
            if vectorstore is not None:
                try:
                    tmp = vectorstore.as_retriever(search_kwargs={"k": max_docs, "filter": metadata_filter})
                    docs = tmp.invoke(query)
                    _LOG.info("[Academic] Filtered retrieval (filter=%s) got %d docs", metadata_filter, len(docs))
                    if not docs:
                        # Filter returned nothing — fall back to unfiltered
                        _LOG.info("[Academic] Filter returned 0 docs — falling back to unfiltered retrieval")
                        docs = self.retriever.invoke(query)
                except Exception as e:
                    _LOG.warning("[Academic] Filtered retrieval failed (%s), falling back to unfiltered", e)
                    docs = self.retriever.invoke(query)
            else:
                docs = self.retriever.invoke(query)
        else:
            docs = self.retriever.invoke(query)

        results: List[Dict[str, Any]] = []
        for d in (docs or [])[:max_docs]:
            results.append(
                {
                    "content": (getattr(d, "page_content", "") or "")[:max_chars],
                    "metadata": getattr(d, "metadata", {}) or {},
                }
            )

        # INFO-level doc visibility log (always visible in production)
        _LOG.info("[Academic] _retrieve_docs query=%r → %d docs returned", query[:60], len(results))
        for i, r in enumerate(results):
            md = r.get("metadata", {}) or {}
            topic = md.get("operation_topic") or md.get("topic") or md.get("filename") or "?"
            etype = md.get("entity_type_normalized") or md.get("entity_type") or ""
            section = md.get("section") or md.get("doc_type") or ""
            snippet = (r.get("content", "") or "")[:80].replace("\n", " ")
            _LOG.info(
                "  [doc %d/%d] topic=%r entity=%r section=%r | %r",
                i + 1, len(results), topic, etype, section, snippet,
            )

        return results

    def _start_intake_with_retrieval(self, state: ConversationState, user_question: str) -> None:
        """
        P0: Retrieve only here (once), store academic_question, set last_retrieval_query.
        FIX: use conf.DEFAULT_RETRIEVAL_FALLBACK_QUERY as single source of truth for fallback.
        Reuse state.current_docs when already populated by supervisor entity-type filter.
        """
        q = (user_question or "").strip()
        if not q:
            q = getattr(conf, "DEFAULT_RETRIEVAL_FALLBACK_QUERY",
                        "กฎหมายร้านอาหาร ใบอนุญาต ภาษี VAT จดทะเบียน สุขาภิบาล ประกันสังคม")

        state.context = state.context or {}

        # Enrich query with entity type if already known (from practical slot memory)
        if hasattr(state, "get_collected_slots"):
            slots = state.get_collected_slots() or {}
            for k, v in slots.items():
                sv = str(v).strip()
                if k == "entity_type" and sv and sv not in q:
                    q = f"{q} {sv}"
                    break

        state.context["academic_question"] = q

        # Decide whether to reuse pre-retrieved docs from supervisor or do a fresh retrieval.
        # Re-retrieve whenever:
        #   1. state.current_docs is empty / too few (< 2 docs)
        #   2. The new academic question is meaningfully different from the query used for
        #      the pre-retrieved docs (topic changed → must fetch relevant docs fresh).
        def _query_changed(new_q: str, state: "ConversationState") -> bool:  # type: ignore[name-defined]
            """Return True when the stored retrieval query doesn't match the new question."""
            if hasattr(state, "get_last_retrieval_query"):
                old_q = (state.get_last_retrieval_query() or "").strip().lower()
            else:
                old_q = (getattr(state, "last_retrieval_query", "") or "").strip().lower()
            nq = new_q.strip().lower()
            if not old_q:
                return True  # nothing stored → must retrieve
            # Use simple keyword-overlap: if < 30 % of significant words overlap → different topic
            old_words = set(w for w in old_q.split() if len(w) > 2)
            new_words = set(w for w in nq.split() if len(w) > 2)
            if not old_words:
                return True
            overlap = len(old_words & new_words) / len(old_words | new_words)
            changed = overlap < 0.30
            if changed:
                _LOG.info(
                    "[Academic] Query changed (overlap=%.0f%%) — will re-retrieve. old=%r new=%r",
                    overlap * 100, old_q[:60], nq[:60],
                )
            return changed

        needs_fresh = len(state.current_docs or []) < 2 or _query_changed(q, state)

        # ✅ Force fresh retrieval when collected slots (entity_type, shop_area_type) were
        # not yet applied to the pre-retrieved docs. Supervisor retrieves unfiltered docs at
        # topic-selection time; academic must re-retrieve with proper metadata filters once
        # the user has answered entity_type / shop_area_type / location_type etc.
        if not needs_fresh and hasattr(state, "get_collected_slots"):
            _slots = state.get_collected_slots() or {}
            _filter_slots = {k: v for k, v in _slots.items()
                             if k in ("entity_type", "shop_area_type", "location_type",
                                      "operation_group", "registration_type")}
            if _filter_slots:
                # Check if current_docs already reflect the entity filter.
                # Many shared docs have entity_type_normalized='' (applies to all entity types).
                # Consider stale ONLY when pre-retrieved docs have NO entity-matched docs
                # and we have MORE than 0 entity-specific docs available.
                _docs_entity = {
                    (d.get("metadata") or {}).get("entity_type_normalized", "").strip()
                    for d in (state.current_docs or [])
                }
                _entity_val = _filter_slots.get("entity_type", "")
                # Pre-retrieved docs are stale when: entity_type is known AND
                # none of the docs carry that entity value (all are unfiltered/empty).
                if _entity_val and _entity_val not in _docs_entity:
                    needs_fresh = True
                    _LOG.info(
                        "[Academic] Force fresh retrieve: entity_type=%r not in docs (docs have %s)",
                        _entity_val, _docs_entity,
                    )
                # ✅ Force fresh when registration_type is known but not yet reflected in the
                # last retrieval query (Chroma has no clean registration_type filter field,
                # so we enrich the query string — but only if the retrieval hasn't done so yet).
                if not needs_fresh:
                    _reg_val = _filter_slots.get("registration_type", "")
                    if _reg_val:
                        _last_q = ""
                        if hasattr(state, "get_last_retrieval_query"):
                            _last_q = (state.get_last_retrieval_query() or "").lower()
                        else:
                            _last_q = (getattr(state, "last_retrieval_query", "") or "").lower()
                        if _reg_val.lower() not in _last_q:
                            needs_fresh = True
                            _LOG.info(
                                "[Academic] Force fresh retrieve: registration_type=%r not in last query %r",
                                _reg_val, _last_q[:80],
                            )

        if not needs_fresh:
            _LOG.info("[Academic] Reusing %d pre-retrieved docs from supervisor", len(state.current_docs))
        else:
            # Build metadata filter from collected slots:
            # - entity_type → $or filter: entity_type_normalized = collected_value OR ''
            #   (shared docs with entity='' apply to all entity types, must NOT be excluded)
            # - shop_area_type / location_type → enrich query string only
            metadata_filter: Optional[Dict[str, Any]] = None
            _q_enrichments: list = []
            if hasattr(state, "get_collected_slots"):
                slots = state.get_collected_slots() or {}
                _entity_val_for_filter = None
                for k, v in slots.items():
                    sv = str(v).strip()
                    if not sv:
                        continue
                    if k == "entity_type":
                        _entity_val_for_filter = sv
                    elif k in ("shop_area_type", "location_type", "operation_group", "registration_type"):
                        if sv not in q:
                            _q_enrichments.append(sv)
                # Use $or so shared docs (entity_type_normalized='') are always included
                if _entity_val_for_filter:
                    metadata_filter = {
                        "$or": [
                            {"entity_type_normalized": _entity_val_for_filter},
                            {"entity_type_normalized": ""},
                        ]
                    }
            if _q_enrichments:
                q = f"{q} {' '.join(_q_enrichments)}".strip()
                _LOG.info("[Academic] Query enriched with slots: %r", _q_enrichments)

            # Multi-topic retrieval: if the question covers multiple distinct topics,
            # retrieve for each sub-topic separately then merge (de-dup by content hash).
            # This ensures "VAT + เหล้า" gets docs for both, not just whichever topic
            # happens to rank highest in a single embedding query.
            _MULTI_TOPIC_PATTERNS = [
                # (keyword_in_question, sub_query_to_retrieve)
                (["vat", "ภาษีมูลค่าเพิ่ม", "ภพ", "ภาษี"],
                 "การจดทะเบียนภาษีมูลค่าเพิ่ม VAT เงื่อนไข ขั้นตอน เอกสาร"),
                (["เหล้า", "สุรา", "แอลกอฮอล์", "เครื่องดื่มแอลกอฮอล์", "ใบอนุญาตสุรา"],
                 "ใบอนุญาตจำหน่ายสุราแอลกอฮอล์ เงื่อนไข ขั้นตอน เอกสาร"),
                (["บุหรี่", "ยาสูบ"],
                 "ใบอนุญาตจำหน่ายยาสูบบุหรี่ เงื่อนไข ขั้นตอน"),
                (["สุขาภิบาล", "สุขลักษณะ", "อาหาร"],
                 "ใบอนุญาตสุขาภิบาลอาหาร ร้านอาหาร เงื่อนไข"),
                (["ประกันสังคม", "ลูกจ้าง", "พนักงาน"],
                 "การขึ้นทะเบียนประกันสังคม นายจ้าง"),
            ]

            q_lower = q.lower()
            matched_sub_queries = []
            for keywords, sub_q in _MULTI_TOPIC_PATTERNS:
                if any(kw in q_lower for kw in keywords):
                    matched_sub_queries.append(sub_q)

            if len(matched_sub_queries) >= 2:
                # Multi-topic: retrieve for each sub-topic and merge (interleaved so each topic
                # is represented when we later take [:_MAX_DOCS_ACADEMIC]).
                _LOG.info("[Academic] Multi-topic question detected — retrieving for %d sub-queries", len(matched_sub_queries))
                seen_hashes: set = set()
                per_topic: list = []  # list of lists
                for sq in matched_sub_queries:
                    sub_docs = self._retrieve_docs(sq, metadata_filter=metadata_filter)
                    per_topic.append(sub_docs)
                # Interleave: take 1 from each topic in round-robin, then de-dup
                merged: list = []
                max_len = max((len(t) for t in per_topic), default=0)
                for i in range(max_len):
                    for topic_docs in per_topic:
                        if i < len(topic_docs):
                            doc = topic_docs[i]
                            h = hash(doc.get("content", "")[:100])
                            if h not in seen_hashes:
                                seen_hashes.add(h)
                                merged.append(doc)
                # Also add broad-query docs that weren't captured by sub-queries
                broad_docs = self._retrieve_docs(q, metadata_filter=metadata_filter)
                for doc in broad_docs:
                    h = hash(doc.get("content", "")[:100])
                    if h not in seen_hashes:
                        seen_hashes.add(h)
                        merged.append(doc)
                state.current_docs = merged
                _LOG.info("[Academic] Multi-topic interleaved merge → %d total docs", len(merged))
            else:
                state.current_docs = self._retrieve_docs(q, metadata_filter=metadata_filter)

        if hasattr(state, "set_last_retrieval_query"):
            state.set_last_retrieval_query(q, cache_to_context=True)
        else:
            state.last_retrieval_query = q
            state.context["last_retrieval_query"] = q

    # ----------------------------
    # Option parsing / binding (ONLY for section menu)
    # ----------------------------
    def _extract_numbered_options(self, text: str) -> Dict[int, str]:
        options: Dict[int, str] = {}
        for m in self._OPTION_LINE_RE.finditer(text or ""):
            try:
                k = int(m.group(1))
                v = (m.group(2) or "").strip()
                if v:
                    options[k] = v
            except Exception:
                continue
        return options

    def _parse_numbers(self, user_text: str) -> List[int]:
        if not user_text:
            return []
        nums = [int(x) for x in re.findall(r"\d{1,2}", user_text)]
        nums = [n for n in nums if 0 < n < 100]
        seen = set()
        out = []
        for n in nums:
            if n not in seen:
                seen.add(n)
                out.append(n)
        return out

    def _is_select_all(self, user_text: str) -> bool:
        return bool(self._SELECT_ALL_RE.search(user_text or ""))

    def _bind_choice_if_any(self, state: ConversationState, user_text: str) -> Dict[str, Any]:
        """
        Binder for SECTION stage only.
        Uses state.context["pending_options"] generated by _ask_sections().
        Supports: numbered selection, "ทั้งหมด", and text-based section name (LLM fallback).
        """
        ctx = state.context or {}
        pending = ctx.get("pending_options") or {}
        if not isinstance(pending, dict) or not pending:
            return {"bound": False}

        if self._is_select_all(user_text):
            keys = sorted([int(k) for k in pending.keys() if str(k).isdigit()])
            resolved = [f"{k}) {pending.get(k)}" for k in keys]
            ctx["resolved_selection"] = {"type": "all", "selected": keys, "resolved": resolved, "raw": user_text}
            ctx["pending_options"] = {}
            ctx["pending_question"] = ""
            state.context = ctx
            return {"bound": True, "mode": "all", "selected": keys}

        nums = self._parse_numbers(user_text)
        if nums:
            valid = [n for n in nums if n in pending]
            if not valid:
                return {"bound": False, "reason": "numbers_not_in_options"}

            # If any selected option is "ทั้งหมด" → treat as select-all
            if any(str(pending.get(n, "")).strip() == "ทั้งหมด" for n in valid):
                keys = sorted([int(k) for k in pending.keys() if str(k).isdigit()])
                resolved = [f"{k}) {pending.get(k)}" for k in keys if str(pending.get(k, "")).strip() != "ทั้งหมด"]
                ctx["resolved_selection"] = {"type": "all", "selected": keys, "resolved": resolved, "raw": user_text}
                ctx["pending_options"] = {}
                ctx["pending_question"] = ""
                state.context = ctx
                return {"bound": True, "mode": "all", "selected": keys}

            resolved = [f"{n}) {pending[n]}" for n in valid]
            ctx["resolved_selection"] = {"type": "numbers", "selected": valid, "resolved": resolved, "raw": user_text}
            ctx["pending_options"] = {}
            ctx["pending_question"] = ""
            state.context = ctx
            return {"bound": True, "mode": "numbers", "selected": valid}

        # No number found — try LLM text-to-section matching
        # e.g., "ขอเรื่องค่าธรรมเนียม" → maps to "ค่าธรรมเนียม" option
        opts = {k: v for k, v in pending.items() if str(k).isdigit()}
        if not opts:
            return {"bound": False}

        opt_list = [str(v) for v in opts.values()]
        matched_n = self._match_section_by_text_llm(user_text, opt_list, list(opts.keys()))
        if matched_n is not None and matched_n in pending:
            # Check if matched to "ทั้งหมด"
            if str(pending.get(matched_n, "")).strip() == "ทั้งหมด":
                keys = sorted([int(k) for k in pending.keys() if str(k).isdigit()])
                resolved = [f"{k}) {pending.get(k)}" for k in keys if str(pending.get(k, "")).strip() != "ทั้งหมด"]
                ctx["resolved_selection"] = {"type": "all", "selected": keys, "resolved": resolved, "raw": user_text}
                ctx["pending_options"] = {}
                ctx["pending_question"] = ""
                state.context = ctx
                return {"bound": True, "mode": "all", "selected": keys}
            resolved = [f"{matched_n}) {pending[matched_n]}"]
            ctx["resolved_selection"] = {"type": "numbers", "selected": [matched_n], "resolved": resolved, "raw": user_text}
            ctx["pending_options"] = {}
            ctx["pending_question"] = ""
            state.context = ctx
            return {"bound": True, "mode": "numbers", "selected": [matched_n]}

        return {"bound": False}

    def _match_section_by_text_llm(self, user_text: str, option_labels: List[str], option_keys: List) -> Optional[int]:
        """
        Use LLM (llm_slots) to match free-text section request to a numbered option.
        Returns the matched option number (int key from pending_options) or None.
        """
        opts_str = "\n".join([f"{k}) {v}" for k, v in zip(option_keys, option_labels)])
        prompt = (
            "หน้าที่: จับคู่ข้อความกับตัวเลือกที่ใกล้ที่สุด\n"
            f"ข้อความผู้ใช้: {user_text}\n"
            f"ตัวเลือก:\n{opts_str}\n"
            "กติกา:\n"
            "- ถ้าข้อความตรงกับตัวเลือกใด ให้คืนเลขตัวเลือกนั้น\n"
            "- ถ้าไม่ตรงกับใดเลย ให้คืน null\n"
            "- ห้ามเดาสุ่ม\n"
            'ตอบ JSON เท่านั้น: {"choice": 1} หรือ {"choice": null}'
        )
        try:
            resp = llm_invoke(self.llm_slots, [HumanMessage(content=prompt)], logger=_LOG, label="Academic/section_bind")
            text = extract_llm_text(resp).strip()
            if "```" in text:
                text = text.split("```")[1].split("```")[0].strip() if text.count("```") >= 2 else text
            obj = json.loads(text)
            if isinstance(obj, dict):
                val = obj.get("choice")
                if val is not None:
                    return int(val)
        except Exception:
            pass
        return None

